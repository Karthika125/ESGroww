const BRAND = {
  green: "#15803d",
  yellow: "#b45309",
  red: "#b91c1c",
  blue: "#1d4ed8",
  slate: "#64748b",
  ink: "#0f172a",
  muted: "#475569",
  line: "#e2e8f0",
  soft: "#f8fafc",
};

const stageColor = (score: number) => {
  if (score >= 90) return BRAND.green;
  if (score >= 75) return "#16a34a";
  if (score >= 60) return BRAND.yellow;
  if (score >= 40) return "#ea580c";
  return BRAND.red;
};

const riskColor: Record<string, string> = {
  Low: BRAND.green,
  Medium: BRAND.yellow,
  "Medium-High": "#ea580c",
  High: BRAND.red,
};

const roadmapColor: Record<string, string> = {
  Immediate: BRAND.red,
  "0–3 Months": "#ea580c",
  "3–6 Months": BRAND.yellow,
  "6–12 Months": BRAND.blue,
  "12+ Months": "#6d28d9",
};

const formatCertName = (name: string) => {
  const map: Record<string, string> = {
    ISO14001: "ISO 14001",
    ISO50001: "ISO 50001",
    NABH: "NABH",
    IGBC: "IGBC Healthcare",
    LEED: "LEED",
    WELL: "WELL",
    BRSR: "BRSR",
    GRI: "GRI",
    CDP: "CDP",
  };
  return map[name] || name;
};

const typeMeta: CSSProperties = {
  fontSize: 9,
  fontWeight: 700,
  letterSpacing: "0.14em",
  textTransform: "uppercase",
  color: BRAND.slate,
  margin: 0,
};

const grid8 = 8;

function Gauge({ value, size = 108 }: { value: number; size?: number }) {
  const radius = 36;
  const circumference = Math.PI * radius;
  const offset = circumference - (value / 100) * circumference;
  const color = stageColor(value);
  return (
    <svg width={size} height={size * 0.62} viewBox="0 0 120 80" aria-hidden="true">
      <path d="M16,64 A36,36 0 0,1 104,64" fill="none" stroke="#e2e8f0" strokeWidth="8" strokeLinecap="round" />
      <path
        d="M16,64 A36,36 0 0,1 104,64"
        fill="none"
        stroke={color}
        strokeWidth="8"
        strokeLinecap="round"
        strokeDasharray={circumference}
        strokeDashoffset={offset}
      />
      <text x="60" y="52" textAnchor="middle" fontSize="22" fontWeight="800" fill={BRAND.ink} fontFamily="Georgia, serif">
        {value}
      </text>
      <text x="60" y="72" textAnchor="middle" fontSize="10" fill={BRAND.muted} fontFamily="system-ui, sans-serif">
        Overall
      </text>
    </svg>
  );
}

